    void sort(double teamscores[]){
        int n = teamscores.length;
        for(int i=0;i< n;i++){
            for(int j=i+1;j<n;j++){
                if(teamscores[j] > teamscores[i]){
                    double temp;
                    temp = teamscores[i];
                    teamscores[i]= teamscores[j];
                    teamscores[j] = temp;
                }
            }
        }
    }
